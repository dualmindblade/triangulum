#!/usr/bin/env python3
"""Mesh<->voxel appearance divergence meter (Austin's image-diff idea).

Renders every pose twice through the play harness — once normal (voxel
patch on), once with `voxels off` (pure heightfield mesh) — and image-diffs
the pairs. Everything outside the voxel patch renders identically (the
harness is deterministic), so the nonzero-diff region IS the patch: its
statistics measure exactly how much appearance the mesh->voxel handoff
changes, per pose, in numbers.

This is a MEASURING INSTRUMENT, not an optimization target: use it to find
the worst poses, to check that a color/texture change moved mesh and blocks
together, and to catch regressions. Do not tune renders to minimize the
metric blindly — muting detail on both sides "improves" the number while
making the game worse (Goodhart). Judgment stays human.

Usage:  python scripts/sync_diff.py [--skip-render] [--label NAME]
        (from viewer/; needs target/release/examples/play.exe built)

Outputs interchange/runs/sync-diff/: the frame pairs, *_diff.png heatmaps,
report.md + report.json (per-pose metrics table).
"""

import argparse
import json
import subprocess
import sys
from pathlib import Path

import numpy as np
from PIL import Image

# The standing pose table. Poses are Austin's own photographed spots plus
